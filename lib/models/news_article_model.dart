// Blueprint for creating news article objects
class NewsArticle {
  final String id;
  final String url;
  final String title;
  final String text;
  final String publisher;
  final String author;
  final String image;
  final String date;

  NewsArticle({
    this.id,
    this.url,
    this.title,
    this.text,
    this.publisher,
    this.author,
    this.image,
    this.date,
  });
}
